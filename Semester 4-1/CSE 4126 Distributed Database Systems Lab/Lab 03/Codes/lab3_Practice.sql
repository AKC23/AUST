clear screen;
SET VERIFY OFF;
SET SERVEROUTPUT ON;


---- DECLARE IT BEFORE DECLARE BLOCK ----
ACCEPT X NUMBER PROMPT "Enter Last 3 digits of ID = "


DECLARE
	A NUMBER;
	B int;

BEGIN
	A := &X;
	B := 0;

	BEGIN
		B := MOD (A,2);
	
	END;
	
	IF B = 0 THEN
		DBMS_OUTPUT.PUT_LINE('EVEN');
	ELSE
		DBMS_OUTPUT.PUT_LINE('EVEN');
	END IF;
	
END;
/

